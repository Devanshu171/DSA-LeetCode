class Solution{
public:
    Node* divide(int N, Node *head){
        // code 
        if(!head || !head->next)
        return head;
        
        Node *odd=new Node(-1),*even=new Node(-1);
        Node *last_odd=odd,*last_even=even;
        Node *cur=head;
        
        while(cur){
            if(cur->data & 1){
                last_odd->next=cur;
                last_odd=cur;
            }else{
                last_even->next=cur;
                last_even=cur;
            }
            cur=cur->next;
        }
        last_even->next=odd->next;
        last_odd->next=NULL;
        return even->next;
    }
};

